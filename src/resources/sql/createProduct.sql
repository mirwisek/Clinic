CREATE TABLE IF NOT EXISTS Product(
 ProductID VARCHAR(10) PRIMARY KEY,
 Type VARCHAR(30),
 Legal BOOLEAN DEFAULT TRUE,
 ProductName VARCHAR(100) NOT NULL,
 Packing VARCHAR(10) NOT NULL,
 Supplier VARCHAR(30) NOT NULL,
 Company VARCHAR(30) NOT NULL,
 ExpiryDate DATE NOT NULL,
 Composition VARCHAR(100) NOT NULL,
 Description VARCHAR(255),
 PriceCarton DECIMAL(10,2) NOT NULL,
 PriceBox DECIMAL(10,2) NOT NULL,
 PriceBP DECIMAL(10,2),
 PricePill DECIMAL(10,2),
 PriceCartonCR DECIMAL(10,2) NOT NULL,
 PriceBoxCR DECIMAL(10,2) NOT NULL,
 PriceBPCR DECIMAL(10,2) NOT NULL,
 PricePillCR DECIMAL(10,2) NOT NULL,
 QtyCarton INT NOT NULL,
 QtyBox INT NOT NULL,
 QtyBP INT DEFAULT 0,
 QtyPill INT DEFAULT 0,
 BoxPCarton INT NOT NULL,
 BPPBox INT NOT NULL,
 PillPBP INT DEFAULT 0,
 Entry DATETIME,
 State VARCHAR(20) DEFAULT 'Unlocked'
);